#!/usr/bin/env bun
// Generates packages/server/src/skill-install/embedded-skills.ts — a map of
// every skill asset (relative path -> content) under packages/server/src/skills.
//
// This is consulted as a FILESYSTEM FALLBACK by the skill readers: in dev and
// in the npm `dist/` layout the real files exist on disk and win; inside a
// `bun build --compile` standalone binary (where dist/skills is not on disk),
// the embedded map supplies the content. Re-run on any skills change (wired
// into the server build).
import { readdirSync, statSync, readFileSync, writeFileSync } from 'node:fs';
import { join, relative, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SKILLS_DIR = join(ROOT, 'packages/server/src/skills');
const OUT = join(ROOT, 'packages/server/src/skill-install/embedded-skills.ts');

function walk(dir) {
  const files = [];
  for (const e of readdirSync(dir)) {
    const full = join(dir, e);
    if (statSync(full).isDirectory()) files.push(...walk(full));
    else if (e.endsWith('.md')) files.push(full);
  }
  return files;
}

const map = {};
for (const f of walk(SKILLS_DIR)) {
  const key = relative(SKILLS_DIR, f).split('\\').join('/'); // e.g. "mma-audit/SKILL.md", "_shared/auth.md"
  map[key] = readFileSync(f, 'utf-8');
}

const body = `// AUTO-GENERATED by scripts/gen-embedded-skills.mjs — do not edit by hand.
// Embedded skill assets (relative path -> content) for the compiled-binary
// distribution, used as a filesystem fallback by the skill readers.
/* eslint-disable */
export const EMBEDDED_SKILLS: Record<string, string> = ${JSON.stringify(map, null, 2)};
`;
writeFileSync(OUT, body);
process.stderr.write(`gen-embedded-skills: wrote ${Object.keys(map).length} assets to ${OUT}\n`);
